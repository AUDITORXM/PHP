			<h2>Breve historia del Origami</h2>

			<p>El <strong>Origami</strong> o <strong>Papiroflexia</strong>, como se lo llama en los países de habla hispana, es un arte que se basa en la técnica del plegado de papel, para obtener figuras y formas decorativas.</p>

			<img src="img/flores.png" alt="Flores">

			<p>El término Origami proviene de las palabras japonesas "ori" plegado y "gami" papel. Es un arte milenario, que Japón desarrolló a partir de la adopción de la invención china del papiro.</p>
			<p>El origami, en su origen, tuvo en Japón un sentido religioso. Más tarde se desarrolló como un pasatiempo y posteriormente, como recurso pedagógico y terapeútico, atrayendo la atención de matemáticos y científicos.</p>
			<p>Aunque en esencia el Origami puede parecer y de hecho es una técnica sencilla, el grado de complejidad que ha alcanzado en el transcurso del tiempo, hace que algunas de sus creaciones requieran días de trabajo.</p>
			<p>En el Origami no se utilizan tijeras, pegamento ni grapas, tan solo el papel y las manos. Aun así, con sólo algunas hojas de papel pueden obtenersa distintas formas geométricas y figuras parecidas a la realidad (animales, personas, flores, objetos, etc).</p>
			<p>El Origami no sólo representa figuras inmóviles, también existen objetos móviles donde las figuras pueden moverse de maneras ingeniosas.</p>
